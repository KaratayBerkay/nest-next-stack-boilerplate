import { cn } from "@/lib/cn";

export const containerClasses = {
  xs: "max-w-xs",
  sm: "max-w-sm",
  md: "max-w-md",
  lg: "max-w-lg",
  xl: "max-w-xl",
  "2xl": "max-w-2xl",
  "3xl": "max-w-3xl",
  "4xl": "max-w-4xl",
  "5xl": "max-w-5xl",
  "6xl": "max-w-6xl",
  "7xl": "max-w-7xl",
  full: "max-w-full",
};

export function getContainerClass(size?: keyof typeof containerClasses) {
  return cn("mx-auto", "w-full", containerClasses[size || "full"], "px-4");
}
